package dfcf

import (
	"encoding/json"
	"fmt"
	"github.com/mymmsc/gox/logger"
	"github.com/mymmsc/gox/util"
	"testing"
	"time"
)

func TestEastmoneyApi_DailyFromDate(t *testing.T) {
	defer logger.FlushLogger()
	api := EastmoneyApi{}
	startTime, _ := time.Parse(util.DateFormat, "2021-08-06")
	kl, dataLastDay, err := api.DailyFromDate("hk00700", startTime)
	if err != nil {
		t.Fatal(err)
	}
	buf, err := json.Marshal(kl)
	fmt.Printf("%+v, %s\n", dataLastDay, string(buf))
	t.Logf("%d", len(kl))
}
